void init()
{
    WDTCTL = WDTPW | WDTHOLD;   // Stop watchdog timer
    // Disable the GPIO power-on default high-impedance mode to activate previously configured port settings.
    PM5CTL0 &= ~LOCKLPM5;       // Lock LPM5.
    __enable_interrupt();
    P4DIR |= BIT0; // make Port 4 pin 0 an output
    P3DIR |=BIT5;
}

void codeTable(void){
    uint8_t i ;
    for(i = 0 ; i < TABLE_LEN ; i++){
        *(LCode+i)    = ( (*code) >> 0) & 0x0f;     // the first 4 digits (length container)
        *(SCode+i)    = ( (*code) >> 4) & 0x0f ;    // the second 4 digits (symbol container)

        code ++;
        *(BCode+i) = *code;                         // the second byte ( binary code container)
        code++;
      }
}

DVAR(uint16_t,  readPt);
DVAR(uint16_t,  writePt);
DVAR(uint16_t, buf);
DVAR(uint16_t, bufIdx);
DVAR(uint8_t,  byte_flag);

void init_task0()
{
    // populate the decoding table
    initData();
    codeTable();
}

void init_task()
{
    WVAR(readPt,   READ_ADDR);       // Write
    WVAR(writePt,  WRITE_ADDR);     // Read
    WVAR(buf,0);
    WVAR(bufIdx ,16);
    WVAR(byte_flag, 1);

    os_block(init_task);
}

void task_read()
{
    uint16_t inbufIdx =             RVAR(bufIdx);
    uint8_t *inreadPt = (uint8_t *) RVAR(readPt);
    uint16_t inbuf    =             RVAR(buf);

    while(inbufIdx >=8 )
    {
        inbuf |= *inreadPt << (inbufIdx - 8);
        inreadPt++;
        inbufIdx -=8;
    }

    WVAR( buf, inbuf ) ;
    WVAR(readPt, (uint16_t) inreadPt);
    WVAR(bufIdx,inbufIdx );

}

void task_write()
{

    uint16_t inbufIdx =              RVAR(bufIdx);
    uint8_t *inwritePt = (uint8_t *) RVAR(writePt);
    uint16_t inbuf =                 RVAR(buf);
    uint8_t inbyte_flag =            RVAR(byte_flag);

    while(inbufIdx < BYTE_LEN)
    {
        uint8_t i;
        for(i=0; i < TABLE_LEN; i++){
            // If there is an exact match between the new byte and a code, output a symbol
            // the extra needed zeros (to form a byte) is added to the MSBs therefore shifting is not required
            if(  ( inbuf & (~0 <<( WORD_LEN - *(LCode+i) ) ) )  == (  ( (uint16_t)*(BCode+i) ) << (WORD_LEN -*(LCode+i) ) ) )
            {
                if(inbyte_flag == 1)
                {
                    (*inwritePt)   = *(SCode+i)<<4 ;
                    inbyte_flag = 0;
                }else{
                    inbyte_flag = 1;
                    (*inwritePt)  |= *(SCode+i) ;
                }

                break;
            }
        }
        inbuf <<= (*(LCode+i)) ;
        inwritePt+=inbyte_flag;
        inbufIdx += *(LCode+i);


        WVAR( buf, inbuf ) ;
        WVAR(writePt, (uint16_t ) inwritePt);
        WVAR(bufIdx,inbufIdx );
        WVAR(byte_flag, inbyte_flag);

        // if decompressing the data is not done
        if( ( inwritePt - ( (uint8_t *) WRITE_ADDR ) ) >=  0x64) // *( (uint16_t *) DATA_LEN ) )
        {
            P3OUT |=BIT5;
            P3OUT &=~BIT5;
            os_unblock(init_task);
//            while(1)
//            {
//                blinkLed(25000 );
//            }
        }

    }
}


int main(int argc, char const *argv[])
{
    init();

    funcPt init_tasks[] = {init_task0};
    os_initTasks(1, init_tasks);

    taskId tasks[] = {  {init_task,0},
                        {task_read,0},
                        {task_write,0 }};
    //This function should be called only once
    os_addTasks(3, tasks );

    os_scheduler();
    return 0;
}

